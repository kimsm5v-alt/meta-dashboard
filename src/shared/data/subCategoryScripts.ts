/**
 * T점수로 레벨 찾기
 */
export const getLevel = (tScore: number): '매우낮음' | '낮음' | '보통' | '높음' | '매우높음' => {
  if (tScore <= 29) return '매우낮음';
  if (tScore <= 39) return '낮음';
  if (tScore <= 59) return '보통';
  if (tScore <= 69) return '높음';
  return '매우높음';
};

/**
 * 중분류명 + T점수 → 해당 스크립트 반환
 */
export const getScript = (subCategory: string, tScore: number): string => {
  const data = SUB_CATEGORY_SCRIPTS[subCategory];
  if (!data) return '';
  
  const level = getLevel(tScore);
  const script = data.scripts.find(s => s.level === level);
  return script?.script || '';
};

export default SUB_CATEGORY_SCRIPTS;
